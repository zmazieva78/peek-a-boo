$(document).ready(function() {
  $(".fadeOut").click(function() {
    $("#walrus").fadeOut();

  });

  $(".fadeIn").click(function() {
    $("#walrus").fadeIn();
  });

  $(".slideDown").click(function() {
    $("#walrus").slideDown();
  });

  $(".slideUp").click(function() {
    $("#walrus").slideUp();
  });

});
